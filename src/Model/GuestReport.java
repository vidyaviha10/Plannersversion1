package Model;

public class GuestReport {

	String name;
	int tableno;
	String seatedWith;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTableno() {
		return tableno;
	}
	public void setTableno(int tableno) {
		this.tableno = tableno;
	}
	public String getSeatedWith() {
		return seatedWith;
	}
	public void setSeatedWith(String seatedWith) {
		this.seatedWith = seatedWith;
	}
	
	
}
